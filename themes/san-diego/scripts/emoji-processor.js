// Helper function to get all available emojis
function getAllEmojis() {
  return { ...standardEmojis, ...customEmojis };
}

// Process emoji in content
function processEmoji(content) {
  const allEmojis = getAllEmojis();

  // Protect code blocks and inline code from emoji processing
  const protectedBlocks = [];
  const protectBlocks = (regex) => {
    content = content.replace(regex, (match) => {
      const placeholder = `__PROTECTED_BLOCK_${protectedBlocks.length}__`;
      protectedBlocks.push(match);
      return placeholder;
    });
  };

  // Protect highlighted code blocks, pre blocks, and inline code
  protectBlocks(/<figure\b[^>]*class="[^"]*highlight[^"]*"[^>]*>[\s\S]*?<\/figure>/gi);
  protectBlocks(/<pre\b[^>]*>[\s\S]*?<\/pre>/gi);
  protectBlocks(/<code\b[^>]*>[\s\S]*?<\/code>/gi);
  
  // Store protected content (like title attributes)
  const protectedContent = [];
  let protectedIndex = 0;
  
  // Protect title attributes from emoji processing
  content = content.replace(/title="([^"]*)"/g, (match, title) => {
    const placeholder = `__PROTECTED_${protectedIndex}__`;
    protectedContent[protectedIndex] = match;
    protectedIndex++;
    return placeholder;
  });
  
  // Regular expression to match :emoji-name: pattern
  const emojiRegex = /:([a-zA-Z0-9_\-\+]+):/g;
  
  content = content.replace(emojiRegex, (match, emojiName) => {
    // Check if it's a standard emoji
    if (standardEmojis[emojiName]) {
      return `<span class="emoji" role="img" aria-label="${emojiName}">${standardEmojis[emojiName]}</span>`;
    }
    
    // Check if it's a custom emoji
    if (customEmojis[emojiName]) {
      // Escape the emoji name for HTML attributes
      const escapedName = escapeHtml(`:${emojiName}:`);
      return `<img class="emoji emoji-custom" src="${customEmojis[emojiName]}" alt="${escapedName}" title="${escapedName}" loading="lazy" decoding="async" />`;
    }
    
    // If emoji not found, return original
    return match;
  });
  
  // Restore protected content
  for (let i = 0; i < protectedContent.length; i++) {
    content = content.replace(`__PROTECTED_${i}__`, protectedContent[i]);
  }

  // Restore protected code blocks
  for (let i = 0; i < protectedBlocks.length; i++) {
    content = content.replace(`__PROTECTED_BLOCK_${i}__`, protectedBlocks[i]);
  }
  
  return content;
}

// Helper function to escape HTML entities
function escapeHtml(text) {
  const map = {
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '"': '&quot;',
    "'": '&#039;'
  };
  return text.replace(/[&<>"']/g, m => map[m]);
}

// Register Hexo filter to process emoji in posts
hexo.extend.filter.register('after_post_render', function(data) {
  data.content = processEmoji(data.content);
  if (data.excerpt) {
    data.excerpt = processEmoji(data.excerpt);
  }
  return data;
});

